<?php

class Solution {

    /**
     * @param Integer $n
     * @return String[]
     */
    function findStrobogrammatic($n) {
        
        if ($n == 2) return ["11","69","88","96"];

        $r = [
            1 => ["0","1","8"],
            2 => ["11","69","88","96"],
        ];

        $h = [1, 6, 8, 9];
        $e = [1, 9, 8, 6];

        if ($n%2 == 0) $m = 4;
        else $m = 3;

        while ($m <= $n) {
            $r[$m] = [];
            for ($i=0; $i<count($r[$m-2]); $i++) {
                for ($j=0; $j<4; $j++) {
                    $r[$m][] = $h[$j].$r[$m-2][$i].$e[$j];
                }
                if ($m>=4) $r[$m][] = $r[$m-2][$i][0]."0".substr($r[$m-2][$i], 1, -1)."0".substr($r[$m-2][$i], -1);
            }
            $m += 2;
        }

        return $r[$n];
    }
}

