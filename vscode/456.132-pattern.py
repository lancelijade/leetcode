class Solution:
    def find132pattern(self, nums: list[int]) -> bool:
        
        mi = [nums[0]]
        i = 1
        while i < len(nums):
            mi.append(min(nums[i], mi[i-1]))
            i += 1
        #print(mi)

        st = []
        i = len(nums) - 1
        while i >= 0:

            #print(f"{i}:", mi[i], st, nums[i])

            if nums[i] == mi[i]:
                i -= 1
                continue
            
            while st and st[-1] <= mi[i]:
                st.pop()

            if st and mi[i] < st[-1] < nums[i]:
                return True

            st.append(nums[i])
            i -= 1

            
        return False
            

        
